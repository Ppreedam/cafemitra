function StructuredSeoCopy({ content, skipFirstH1 = false }: { content: string; skipFirstH1?: boolean }) {
  const blocks = content
    .replace(/(^|\n)(H[123]: [^\n]+)\n/g, "$1\n$2\n\n")
    .split(/\n{2,}/)
    .map((block) => block.trim())
    .filter(Boolean);

  let firstH1Skipped = false;
  return (
    <>
      {blocks.map((block, index) => {
        const lines = block.split("\n").map((line) => line.trim()).filter(Boolean);
        const first = lines[0] || "";

        if (first.startsWith("H1: ")) {
          if (skipFirstH1 && !firstH1Skipped) {
            firstH1Skipped = true;
            return null;
          }
          return <h1 key={index}>{first.slice(4)}</h1>;
        }
        if (first.startsWith("H2: ")) return <h2 key={index}>{first.slice(4)}</h2>;
        if (first.startsWith("H3: ")) return <h3 key={index}>{first.slice(4)}</h3>;

        return (
          <div className={index === 1 ? "tool-seo-copy-paragraph tool-seo-hero" : "tool-seo-copy-paragraph"} key={index}>
            {renderLines(lines, `${index}`)}
          </div>
        );
      })}
    </>
  );
}

function renderLines(lines: string[], keyPrefix: string): ReactNode[] {
  const output: ReactNode[] = [];
  let index = 0;
  while (index < lines.length) {
    const line = lines[index];

    if (line.startsWith("✓")) {
      output.push(
        <div className="tool-seo-badges" key={`${keyPrefix}-badges-${index}`}>
          {line.split(/\s{2,}/).map((item) => <span key={item}>{item}</span>)}
        </div>,
      );
      index += 1;
      continue;
    }

    if (isCallout(line)) {
      output.push(<aside className="tool-seo-callout" key={`${keyPrefix}-callout-${index}`}><p>{renderInline(line)}</p></aside>);
      index += 1;
      continue;
    }

    if (/^\d+\.\s/.test(line)) {
      const items: string[] = [];
      while (index < lines.length && /^\d+\.\s/.test(lines[index])) {
        items.push(lines[index].replace(/^\d+\.\s*/, ""));
        index += 1;
      }
      output.push(<ol className="tool-seo-list" key={`${keyPrefix}-steps-${index}`}>{items.map((item) => <li key={item}>{renderInline(item)}</li>)}</ol>);
      continue;
    }

    if (line.startsWith("•")) {
      const items: string[] = [];
      while (index < lines.length && lines[index].startsWith("•")) {
        items.push(lines[index].replace(/^•\s*/, ""));
        index += 1;
      }
      output.push(<ul className="tool-seo-list" key={`${keyPrefix}-list-${index}`}>{items.map((item) => <li key={item}>{renderInline(item)}</li>)}</ul>);
      continue;
    }

    if (line.startsWith("➜")) {
      const label = line.replace(/^➜\s*/, "");
      const helper = lines[index + 1] && !isSpecialLine(lines[index + 1]) ? lines[index + 1] : "";
      output.push(
        <div className="tool-seo-cta-stack" key={`${keyPrefix}-cta-${index}`}>
          <a className="tool-seo-inline-cta" href={label.startsWith("See How PrintPilot") ? "/print-automation" : "/pdf-tools/extract-pages"}>{label} <span>→</span></a>
          {helper ? <p>{helper}</p> : null}
        </div>,
      );
      index += helper ? 2 : 1;
      continue;
    }

    output.push(<p key={`${keyPrefix}-paragraph-${index}`}>{renderInline(line)}</p>);
    index += 1;
  }
  return output;
}

function isSpecialLine(line: string) {
  return line.startsWith("H") || line.startsWith("✓") || line.startsWith("➜") || line.startsWith("•") || isCallout(line) || /^\d+\.\s/.test(line);
}

function isCallout(line: string) {
  return /^[📊💡🔒✅⚠️]/.test(line);
}

function renderInline(text: string): ReactNode[] {
  const patterns = /(Remove Pages tool|security page|PrintPilot)/g;
  return text.split(patterns).map((part, index) => {
    if (part === "Remove Pages tool") return <a href="/pdf-tools/remove-pages" key={`${part}-${index}`}>Remove Pages tool</a>;
    if (part === "security page") return <a href="/privacy-policy" key={`${part}-${index}`}>security page</a>;
    if (part === "PrintPilot") return <a href="/print-automation" key={`${part}-${index}`}>PrintPilot</a>;
    return part;
  });
}

